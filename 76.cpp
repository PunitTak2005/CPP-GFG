#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 12, b = 6;
    
    a += b;
    cout << "Add & Assign: "<< a <<"\n";
    
    a -= b;
    cout << "Subtract & Assign: " << a << "\n";
    
    a *= b;
    cout << "Multiply & Assign: " << a << "\n";
    
    a /= b;
    cout << "Divide & Assign: " << a << "\n"; 
    
    return 0;
}
